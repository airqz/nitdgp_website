            <h2 style="color: #FFF;width: 100%;" class="headtitle">Principal Investigator (PI)</h2>
            <p style="padding: 10px;text-align: justify;">
              The term Principal Investigator (PI) refers to a faculty member of the Institute who prepares and submits the project proposal through proper channel and takes full responsibility of carrying out the project to completion along with the achievement of project goals. As a Principal Investigator, one should take into account his professional interest in identifying new and important research questions. One could establish an internal review panel as a team to discuss the efficacy of the proposed study. The team will concentrate on the improvement of scientific content of the proposal. The PI may be assisted by one or more Co-investigators (CI) for achieving the technical goals. The CI(s) will ordinarily be permanent faculty members of the  Institute. In case of Inter-institutional projects the PI of the second institution will be called Co-PI. 
              PI of a sanctioned project is considered to be the prime interface between the funding agency and SRCC and is operational head of the project. The PI is advised to keep the whole project team aware about the progress of the project and involve the CI(s) in all related project activities. In his/her absence, the PI may request any of the CI to take-over the responsibility of running the project (with or without financial responsibility) and inform the same to SRCC. When the period of leave is likely to exceed one year, the sponsors must be informed by the PI.
              PI /Co-PI/CI(s) can claim honorarium in a project only if it is explicitly provided by the Funding Agency.
              PI/Co-PI/CI(s) is permitted to go outside the Institute on Special Casual Leave with prior approval of competent authority, for work related to Projects, but without affecting academic activities in the Institute. The PI shall be responsible for submission of periodical and /or final technical report(s) of the project work as may be required by the sponsoring agency. He / She shall also send a copy of the final technical report to Dean (R&C). In the consultancy projects, the principal consultant and other consultants will have the same responsibility and authority as of PI and CI of sponsored research projects.
