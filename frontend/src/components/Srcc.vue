            <h2 style="color: #FFF;width: 100%;" class="headtitle">Sponsored Research</h2>
            <p style="padding: 10px;text-align: justify;">
              <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Project Proposal</u> : </span>
              All the project proposals to different sponsoring agencies have to be prepared as per the following format or as per the format prescribed by the Sponsoring agency:<br>
              <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Title</u>:</span>
               The project title shall be a good match to the proposed themes of the funding agency. Moreover, the reviewer should easily remember it. <br>
               <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Summary of the Proposal:</u></span>
                One needs to highlight the aims and overview of the research plan. It should demonstrate the importance of the study and generate interest to the reviewer.<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Research Statement</u>:</span>
                PI should incorporate the relevance of the proposed study, the focus of the study and a rational plan to achieve the study goal(s).<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Objectives</u>:</span>
                The proposal should establish the overall direction of the proposed study. In the case of several objectives PI should state the action, the behavior and the standard against which it will be measured.<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Literature Review</u>:</span>
                PI needs to incorporate important work in a concise manner in the proposed study. In addition, PI should try to advance the knowledge.<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Study Design</u> :</span>
                It shall be simple and clear. Moreover, the design should be based on the study objectives. You may need to discuss with the expert in research methods, if necessary, to obtain suggestions on sample design, data measurement, collection and analysis.<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>IPR issues</u> :</span>
                The information generated under the projects is usually the joint property of the Funding Agency and the Institute unless otherwise stated in the Contract/Agreement with a funding agency or a collaborative partner with whom the ownership issue is mutually decided beforehand. The same may be protected by means of filing of copyrights/patents etc. Funding agencies are concerned with ethical issues of the scientific experimentation. PI is responsible for upholding academic integrity and having the requisite clearance from the ethical committee as required.<br>
                <span style="font-size: 18px;font-weight: bold;color: #01576A;"><u>Budget Estimate</u> :</span>
                While making a budget for a project, the following heads should be taken into account:
              <ul type="square">
                <li>Salary of Project Staff (JRF/SRF/Project staff)</li>
                <li>Equipment</li>
                <li>Consumables</li>
                <li>Travel (domestic, international)</li>
                <li>Contingency</li>
                <li>Overheads</li>
              </ul>
                  In general, for sponsored research projects 20% on the total project cost shall be charged towards service charges (overheads) for utilizing the infrastructural and other facilities of the Institute. The Institute shall support funding for filling patent and fees related to their maintenance. All research proposals shall be routed through and endorsed by Dean (R&C) on behalf of the Director, with relevant information required by Sponsors. In case of projects requiring significant infrastucture facilities and additional support by the Institute, Dean (R&C) will obtain the prior approval of the Director. The entire outgoing project proposals will be recorded in the proposal register and be filed for documentation of proposals sent to sponsoring agencies for approval. All sanctioned and approved project proposals have to be registered with the SRCC by filiing up of „Project Registration Form”, duly forwarded by HOD/HOC along with a copy of the proposal (both original and recast, if any) and approval by the sponsors for project registration. It shall be the responsibility of the PI to get the project work completed satisfactorily within the sanctioned grant and duration.
            </p>
            <h4 style="font-weight: bold;color: #01576A;"><u>Project Staff Recruitment</u></h4>
            <p style="padding: 10px;text-align: justify;">
              All the project employees (JRF/SRF/RA/Project staff) will be appointed through open advertisement followed by the interviews. SRCC provides following options of recruiting project staff to kick-start the project activities:<br>
              <span style="font-weight: bold;color: #01576A;"><u>Casual Appointments</u> </span><br>
              The PI may immediately offer a contract of work on casual basis (informal selection) to anyone with suitable qualifications starting from the date of their choice. However, such awards are restricted to 89 days only for each individual. These may be given against sanctioned positions. In the meantime, the process for project appointments against the sanctioned posts in the project can be initiated.<br>
              <span style="font-weight: bold;color: #01576A;"><u>Casual Appointments</u> </span><br>
              These appointments are purely contractual and renewed every year maximum up to the duration of the project. On receipt of the project, the PI shall forward the request for such engagements to SRCC. SRCC will post the advertisement on Institute website and SRCC notice board. Usually there should be a time gap of about 3 weeks between the advertisement and the last date for receiving the completed application. After the due date the PI will scrutinize the aplications received, short list the candidates and get it approved by Dean (R&C). The usual time gap between the dispatch of call letter and the date of interview should be about 3 weeks but can be reduced if all short listed candidates can be contacted effectively. The project appointees will have to sign an undertaking.<br>The PI has two options for holding these selections:<br><b>Option I -</b> Walk in test/ Interview; and <br><b> Option II -</b> Short listing followed by test and / or Interview. 
              Selection of JRF/ SRF/ RA shall normally be made as per the guidelines provided by the sponsoring agency and with the approval of the Dean (R&C). Automatic transfer from one project to another either on completion or midway shall not be permitted, except with specific permission from Dean (R&C)/Director. The PIs shall consider prevailing compensation package, general qualifications and experience for staff while preparing project proposal.<br>
              <span style="font-weight: bold;color: #01576A;"><u>Selection Committee</u> </span><br>
              The Selection Committee for the recruitment of project staff for each project shall consist of following:<br>
              <ul>
              <li>HOD/HOC of the concerned Dept./ Centre to which the PI belongs - Chairperson </li>
              <li>One faculty member of the PI‟s Dept. - Member </li>
              <li>One subject expert from outside the Institute as recommended by PI - Member </li>
              <li>Principal Investigator/Co-Investigator - Convenor </li>
            </ul><br>
            Scheduled selection committees cannot be cancelled at short notice in order to avoid inconvenience to appearing candidates especially coming from outside. <br>All appointment letters shall be issued under signature of Assistant Registrar (SR&C).<br>
            <span style="font-weight: bold;color: #01576A;"><u>Assistantships/Fellowship</u> </span><br>
