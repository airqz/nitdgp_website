            <span style="font-weight: bold;color: #01576A;"><u>Appointing Visiting Scientists</u> </span><br>As per project approval/guidelines the PIs can appoint visiting scientists by invitation against a sanctioned post in a project or from specific project head with the prior approval of Director/Dean (R&C).<br>
            <span style="font-weight: bold;color: #01576A;"><u>Conduct Rules</u> </span><br>A project employee shall execute a Contract Agreement with SRCC at the time of joining with the explicit provision that contract may be terminated by either side (Staff or SRCC Office) by giving one month's notice or one month's consolidated compensation in lieu of the notice. <br>
            PIs shall be the sanctioning and controlling authority for all types of leaves with respect to contractual project staff. 
            Project employees shall <br>
            a) Follow general code of conduct as approved by SRCC.<br> 
            b) Maintain secrecy of the research findings/technical information and shall not get involved in unauthorized communication of any official document or information.<br>
            Dean (R&C) may, at his discretion, constitute committee(s) to conduct disciplinary proceedings, if necessary against project employees. On the basis of the report suitable disciplinary action may be initiated and punishment will be imposed by the Dean (R&C). <br>
